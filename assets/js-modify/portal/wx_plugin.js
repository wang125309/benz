$(function(){
    $.post("/benz/portal/wxconfig/",{
		"url":location.href
	},
	function(data){
		wx.config(data);
		wx.ready(function(){
            wx.hideOptionMenu();
        });
		wx.error(function(){
            $.get("/benz/portal/update_access_token/",function(){
			    $.post("/benz/portal/wxconfig/",{
				    "url":location.href
			    },function(data){
				    wx.config(data);
				    wx.ready(function(){
                        wx.hideOptionMenu();
				    });
			    });
            });
		});
	});
});
